//支持自动换行
//(x,y):起始坐标
//width,height:区域
//str  :字符串
//f_w:字宽
//f_h:字高
//mode:0,非叠加方式;1,叠加方式
void OLED_Show_Str(uint16_t x,uint16_t y,char *str,uint8_t f_h,uint8_t mode)
{
    uint16_t x0=x;
    uint16_t y0=y;
    uint8_t bHz=0;      //字符或者中文
    while(*str!=0)      //数据未结束
    {
        if(!bHz)
        {
            if((unsigned char)(*str)>0x80)
            {
                bHz=1;//中文
            }
            else               //字符
            {
                if(x>(x0+OLED_W - f_h / 2))//换行
                {
                    y+=f_h;
                    x=x0;
                }
                if(y>(y0+OLED_H-f_h))break;//越界返回
                if(*str==13)//换行符号
                {
                    y+=f_h;
                    x=x0;
                    str++;
                }
                else OLED_ShowChar(x,y,*str,f_h,mode);//有效部分写入
                str++;
                x+=f_h/2; //字符,为全字的一半
            }
        }
        else//中文
        {
            bHz=0;//有汉字库
            if(x>(x0+OLED_W -f_h))//换行
            {
                y+=f_h+2;//2为行间距
                x=x0;
            }
            if(y>(y0+OLED_H-f_h))break;//越界返回
            OLED_Show_Font(x,y,str,f_h,f_h,mode); //显示这个汉字,空心显示
            str+=2;
            x+=f_h;//下一个汉字偏移
        }
    }
}

