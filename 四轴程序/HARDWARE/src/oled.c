//OLED显存
uint8_t OLED_GRAM[OLED_W][OLED_H / 8];

//更新显存到LCD
void OLED_Refresh_Gram(void)
{
    uint16_t i,n;
    
    for(i=0; i<8; i++)
    {
        OLED_WR_Byte (0xb0+i,OLED_CMD);    //设置页地址（0~7）
        OLED_WR_Byte (0x02,OLED_CMD);      //设置显示位置―列低地址    //1.3寸oled 0x02
        OLED_WR_Byte (0x10,OLED_CMD);      //设置显示位置―列高地址
        for(n=0; n<OLED_W; n++)
        {
            OLED_WR_Byte(OLED_GRAM[n][i],OLED_DATA);
        }
    }
}

