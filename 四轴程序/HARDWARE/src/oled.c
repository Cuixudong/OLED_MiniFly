{
    OLED_DrawLine(x1,y1,x2,y1,dot);
    OLED_DrawLine(x1,y1,x1,y2,dot);
    OLED_DrawLine(x1,y2,x2,y2,dot);
    OLED_DrawLine(x2,y1,x2,y2,dot);
}
