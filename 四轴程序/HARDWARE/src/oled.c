void Structure_3D(float MAT[4][4])
{
    MAT[0][0]=1;
    MAT[0][1]=0;
    MAT[0][2]=0;
    MAT[0][3]=0;// 1 0 0 0
    MAT[1][0]=0;
    MAT[1][1]=1;
    MAT[1][2]=0;
    MAT[1][3]=0;// 0 1 0 0
    MAT[2][0]=0;
    MAT[2][1]=0;
    MAT[2][2]=1;
    MAT[2][3]=0;// 0 0 1 0
    MAT[3][0]=0;
    MAT[3][1]=0;
    MAT[3][2]=0;
    MAT[3][3]=1;// 0 0 0 1
}

void MATRIX_copy(float sourceMAT[4][4],float targetMAT[4][4])
{
    int a,b;
    for(a=0; a<4; a++)
    {
        for(b=0; b<4; b++)
        {
            targetMAT[a][b]=sourceMAT[a][b];
        }
    }
}

void MATRIX_multiply(float MAT1[4][4],float MAT2[4][4],float newMAT[4][4])
{
    int a,b;
    for(a=0; a<4; a++)
    {
        for(b=0; b<4; b++)
        {
            newMAT[a][b]=MAT1[a][0]*MAT2[0][b]+MAT1[a][1]*MAT2[1][b]+\
                    MAT1[a][2]*MAT2[2][b]+MAT1[a][3]*MAT2[3][b];
        }
    }
}

void Translate3D(float MAT[4][4],int16_t tx,int16_t ty,int16_t tz)
{
    float lMAT[4][4];
    float tMAT[4][4];
    tMAT[0][0]=1;
    tMAT[0][1]=0;
    tMAT[0][2]=0;
    tMAT[0][3]=tx;	//	1 0 0 tx
    tMAT[1][0]=0;
    tMAT[1][1]=1;
    tMAT[1][2]=0;
    tMAT[1][3]=ty;	//	0 1 0 ty
    tMAT[2][0]=0;
    tMAT[2][1]=0;
    tMAT[2][2]=1;
    tMAT[2][3]=tz;	//	0 0 1 tz
    tMAT[3][0]=0;
    tMAT[3][1]=0;
    tMAT[3][2]=0;
    tMAT[3][3]=1;	//	0 0 0 1
    MATRIX_multiply(MAT,tMAT,lMAT); //相乘
    MATRIX_copy(lMAT,MAT);          //复制
}

void  Scale_3D(float MAT[4][4],float sx,float sy,float sz)
{
    float lMAT[4][4];
    float tMAT[4][4];
    tMAT[0][0]=sx;
    tMAT[0][1]=0;
    tMAT[0][2]=0;
    tMAT[0][3]=0;	//	sx0 0 0
    tMAT[1][0]=0;
    tMAT[1][1]=sy;
    tMAT[1][2]=0;
    tMAT[1][3]=0;	//	0 sy0 0
    tMAT[2][0]=0;
    tMAT[2][1]=0;
    tMAT[2][2]=sz;
    tMAT[2][3]=0;	//	0 0 sz0
    tMAT[3][0]=0;
    tMAT[3][1]=0;
    tMAT[3][2]=0;
    tMAT[3][3]=1;	//	0 0 0 1
    MATRIX_multiply(MAT,tMAT,lMAT);	//相乘
    MATRIX_copy(lMAT,MAT);          //复制
}

void Rotate_3D(float MAT[4][4],float ax,float ay,float az)
{
    float MAT1[4][4];
    float MAT2[4][4];
    float MATx[4][4];
    float MATy[4][4];
    float MATz[4][4];

    ax = D2RVAL * ax;	//角度转换为弧度量
    ay = D2RVAL * ay;
    az = D2RVAL * az;
    /*****************************绕x轴旋转********************************************/

    MATx[0][0]=1;
    MATx[0][1]=0;
    MATx[0][2]=0;
    MATx[0][3]=0;//1        0        0              0
    MATx[1][0]=0;
    MATx[1][1]=cos(ax);
    MATx[1][2]=-sin(ax);
    MATx[1][3]=0;//0        cos(ax)  -sin(ax)       0
    MATx[2][0]=0;
    MATx[2][1]=sin(ax);
    MATx[2][2]=cos(ax);
    MATx[2][3]=0;//0        sin(ax)   cos(ax)       0
    MATx[3][0]=0;
    MATx[3][1]=0;
    MATx[3][2]=0;
    MATx[3][3]=1;//0        0        0              1

    /*****************************绕y轴旋转********************************************/
    MATy[0][0]=cos(ay);
    MATy[0][1]=0;
    MATy[0][2]=sin(ay);
    MATy[0][3]=0;//cos(ay)  0     sin(ay)   0
    MATy[1][0]=0;
    MATy[1][1]=1;
    MATy[1][2]=0;
    MATy[1][3]=0;//0        1     0         0
    MATy[2][0]=-sin(ay);
    MATy[2][1]=0;
    MATy[2][2]=cos(ay);
    MATy[2][3]=0;//-sin(ay) 0     cos(ay)   0
    MATy[3][0]=0;
    MATy[3][1]=0;
    MATy[3][2]=0;
    MATy[3][3]=1;//0        0     0         1

    /*****************************绕z轴旋转********************************************/
    MATz[0][0]=cos(az);
    MATz[0][1]=-sin(az);
    MATz[0][2]=0;
    MATz[0][3]=0;//cos(az) -sin(az)  0       0
    MATz[1][0]=sin(az);
    MATz[1][1]=cos(az);
    MATz[1][2]=0;
    MATz[1][3]=0;//sin(az)  cos(az)  0       0
    MATz[2][0]=0;
    MATz[2][1]=0;
    MATz[2][2]=1;
    MATz[2][3]=0;//0       0         1       0
    MATz[3][0]=0;
    MATz[3][1]=0;
    MATz[3][2]=0;
    MATz[3][3]=1;//0       0         0       1

    MATRIX_multiply(MAT,MATx,MAT1);
    MATRIX_multiply(MAT1,MATy,MAT2);
    MATRIX_multiply(MAT2,MATz,MAT);
}

point_3d vector_matrix_MULTIPLY(point_3d Source,float MAT[4][4])
{
    point_3d Result;
    Result.x=Source.x*MAT[0][0]+Source.y*MAT[1][0]+Source.z*MAT[2][0]+MAT[3][0];
    Result.y=Source.x*MAT[0][1]+Source.y*MAT[1][1]+Source.z*MAT[2][1]+MAT[3][1];
    Result.z=Source.x*MAT[0][2]+Source.y*MAT[1][2]+Source.z*MAT[2][2]+MAT[3][2];
    return Result;
}

point_2d OrtProject(point_3d Space)
{
    point_2d Screen;
    Screen.x=(int16_t)Space.x;
    Screen.y=(int16_t)Space.y;
    return Screen;
}

#define FOCAL_DISTANCE 1024  //视点到平面的距离

point_2d PerProject(point_3d Space,int16_t XO,int16_t YO)
{
    point_2d Screen;
    if(Space.z == 0)
    Space.z = 1;
    Screen.x = (int16_t)(FOCAL_DISTANCE * Space.x /(Space.z + FOCAL_DISTANCE) + XO);
    Screen.y = (int16_t)(FOCAL_DISTANCE * Space.y /(Space.z + FOCAL_DISTANCE) + YO);
    return Screen;
}

