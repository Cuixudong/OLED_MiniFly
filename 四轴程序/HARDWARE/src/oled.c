static uint8_t registerRead(uint8_t reg)
{
    uint8_t data = 0;
    spiBeginTransaction();
    spiExchange(1, &reg, &data);
    spiEndTransaction();
    return data;
}

//向SSD1306写入一个字节。
//dat:要写入的数据/命令
//cmd:数据/命令标志 0,表示命令;1,表示数据;
void OLED_WR_Byte(uint8_t dat,uint8_t cmd)
{
    uint8_t i;
    if(cmd) //写命令
    {
        OLED_DC_H;
    }
    else
    {
        OLED_DC_L;
    }
    OLED_CS_L;
    #if (!USB_HW_SPI)
    for(i=0;i<8;i++)
    {
        OLED_D0_L;
    
        if(dat&0x80)
            OLED_D1_H;
        else 
            OLED_D1_L;
    
        OLED_D0_H;
    
        dat<<=1;
    }
    #else
    i = dat;
    //HAL_SPI_Transmit(&hspi2,&i,1,0xFF);
    registerRead(i);
    #endif
    OLED_CS_H;
    OLED_DC_H;
}

